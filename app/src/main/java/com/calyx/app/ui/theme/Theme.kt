@Composable
fun CalyxTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Disable dynamic color to use our custom green palette
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    // Always use our custom color scheme for consistent branding
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme
    
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // Use ForestGreen for status bar (header gradient start)
            window.statusBarColor = ForestGreen.toArgb()
            // Light status bar icons on green background
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = false
            
            // Navigation bar styling
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
                window.navigationBarColor = colorScheme.surface.toArgb()
                WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
